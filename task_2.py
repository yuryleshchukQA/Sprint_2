class Movies:
    def __init__(self):
        self.movies = []
    def add_movie(self, movies):
        self.movies.append(movies)

class Comedy(Movies):
    def add_movie(self,movies):
        super().add_movie(movies)
        return f'Комедии: {self.movies}'

class Drama(Movies):
    def add_movie(self, movies):
        super().add_movie(movies)
        return f'Драмы: {self.movies}'

comedy = Comedy()
print(comedy.add_movie('Большой куш'))
drama = Drama ()
print(drama.add_movie('Оружейный барон'))


